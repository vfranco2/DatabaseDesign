CREATE TABLE IF NOT EXISTS`Recipes` (
  `recipeID` INT(10) UNSIGNED NOT NULL AUTO_INCREMENT,
  `recipeName` VARCHAR(128) NULL DEFAULT NULL,
  `recipeLink` TEXT,
  PRIMARY KEY (`recipeID`));

CREATE TABLE IF NOT EXISTS`ingredients` (
  `ingredientid` INT(10) NOT NULL AUTO_INCREMENT,
  `ingredientName` VARCHAR(45) DEFAULT NULL,
  `ingredientLink` VARCHAR(128) DEFAULT NULL,
  `heldAmt` DECIMAL(4,2) NULL DEFAULT NULL,
  PRIMARY KEY (`ingredientid`));
  
  CREATE TABLE IF NOT EXISTS`users` (
  `userID` INT(10) NOT NULL AUTO_INCREMENT,
  `userName` VARCHAR(45) DEFAULT NULL,
  PRIMARY KEY (`userID`));

CREATE TABLE IF NOT EXISTS`RefTable` (
  `recipeID` INT(10) UNSIGNED NOT NULL,
  `ingredientid` INT(10) NOT NULL,
  CONSTRAINT PK_RefTable PRIMARY KEY
  (
  recipeID,
  ingredientid
  ),
  FOREIGN KEY (recipeID) REFERENCES Recipes (recipeID),
  FOREIGN KEY (ingredientid) REFERENCES ingredients (ingredientid),
  `reqAmt` DECIMAL(4,2) NULL DEFAULT NULL,
  INDEX (`recipeID`)
  );
  
  CREATE TABLE IF NOT EXISTS`UserIngs` (
  `userID` INT(10) NOT NULL,
  `ingredientid` INT(10) NOT NULL,
  CONSTRAINT PK_RefTable PRIMARY KEY
  (
  userID,
  ingredientid
  ),
  FOREIGN KEY (userID) REFERENCES users (userID),
  FOREIGN KEY (ingredientid) REFERENCES ingredients (ingredientid),
  `userAmt` DECIMAL(4,2) NULL DEFAULT NULL,
  INDEX (`userID`)
  );


